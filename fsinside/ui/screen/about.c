void about() {
	THREAD_LOCK;
	isAboutOpen = true;
	int ret;
	pthread_t draw_bean_thread;

	bean_loop = true;
	init_about_color();
	about_window = newwin(0, 0, 0, 0);
	box(about_window, 0, 0);
	werase(about_window);
	touchwin(about_window);
	wborder(about_window, '#', '#', '#', '#', '#', '#', '#', '#');
	wrefresh(about_window);

	// Draw bean
	bean_window = newwin(50, 76, 4, 6);
	WINDOW* bean_window_frame = newwin(51, 80, 2, 3);
	box(bean_window_frame, 0, 0);	
	wborder(bean_window_frame, '#', '#', '#', '#', '#', '#', '#', '#');
	wrefresh(bean_window);	
	wrefresh(bean_window_frame);	

	ret = pthread_create(&draw_bean_thread, 0, draw_bean, 0);
	ret = wgetch(about_window);
	bean_loop = false;
	pthread_join(draw_bean_thread, NULL);

	// Remove all windows and restore previous screen
	wclear(bean_window);
	delwin(bean_window);
	wclear(about_window);
	delwin(about_window);
	wclear(bean_window_frame);
	delwin(bean_window_frame);
	redrawwin(stdscr);
	isAboutOpen = false;
	THREAD_UNLOCK;
}

